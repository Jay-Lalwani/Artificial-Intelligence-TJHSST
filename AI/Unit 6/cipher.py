POPULATION_SIZE = 500
NUM_CLONES = 1
TOURNAMENT_SIZE = 20
TOURNAMENT_WIN_PROBABILITY = .75
CROSSOVER_LOCATIONS = 5
MUTATION_RATE = .8

def breed(parents):
    parent1 = parents[0][1]
    parent2 = parents[1][1]
    crossovers = random.sample(range(len(parent1)), CROSSOVER_LOCATIONS)
    child = ['.' for i in range(len(parent1))]
    for index in crossovers:
        child[index] = parent1[index]
    for c in parent2:
        if c not in child:
            i = child.index('.')
            child[i] = c
    if random.random() < MUTATION_RATE:
        mutations = random.sample(range(len(child)), 2)
        temp = child[mutations[0]]
        child[mutations[0]] = child[mutations[1]]
        child[mutations[1]] = temp
    child = ''.join(child)
    return child
    
    
def fitPop(enc, population, ngrams):
    oldPop = []
    for p in population:
        oldPop.append((fitness(ngrams, enc, p), p))
    oldPop = sorted(oldPop)[::-1]
    return oldPop

def selectAndBreed(oldPop):
    nextGen = set()
    for i in range(NUM_CLONES):
        nextGen.add(oldPop[i][1])
    while len(nextGen) != POPULATION_SIZE:
        tournaments = random.sample(oldPop, 2*TOURNAMENT_SIZE)
        tournament1 = sorted(tournaments[:TOURNAMENT_SIZE])[::-1]
        tournament2 = sorted(tournaments[TOURNAMENT_SIZE:])[::-1]
        parents = []
        while not parents:
            if len(tournament1) == 1 or random.random() < TOURNAMENT_WIN_PROBABILITY:
                parents.append(tournament1[0])
            else: tournament1 = tournament1[1:]
        while len(parents) == 1:
            if len(tournament2) == 1 or random.random() < TOURNAMENT_WIN_PROBABILITY:
                parents.append(tournament2[0])
            else: tournament2 = tournament2[1:]
        child = breed(parents) 
        nextGen.add(child)
    nextGen = list(nextGen)
    return nextGen
    

def geneticAlgorithm(enc, iterations, ngrams):
    gen0 = set()
    while len(gen0) < POPULATION_SIZE:
        myCipher = list(alphabet)
        random.shuffle(myCipher)
        myCipher = ''.join(myCipher)
        gen0.add(myCipher)
    nextGen = fitPop(enc, list(gen0), ngrams)
    for i in range(iterations):
        nextGen = fitPop(enc, selectAndBreed(nextGen), ngrams)
        print(decode(enc, nextGen[0][1]))
