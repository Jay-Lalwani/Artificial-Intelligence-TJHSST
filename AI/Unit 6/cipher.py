def encode(text, cipher):
    text = text.upper()
    encodedMessage = ''
    for t in text:
        cipherIndex = alphabet.find(t)
        if cipherIndex == -1:
            encodedMessage += t
        else:
            encodedMessage += cipher[cipherIndex]
    return encodedMessage

def decode(text, cipher):
    text = text.upper()
    decodedMessage = ''
    for t in text:
        alphaIndex = cipher.find(t)
        if alphaIndex == -1:
            decodedMessage += t
        else:
            decodedMessage += alphabet[alphaIndex]
    return decodedMessage

def fitness(n, enc, cipher):
    dec = decode(enc, cipher)
    sum = 0.0
    for i in range(len(dec) - n):
        ngram = dec[i:i+n]
        if ngram.isalpha() and ngram in ngramsDic:
            sum += log(ngramsDic[ngram], 2)
    return sum

